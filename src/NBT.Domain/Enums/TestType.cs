namespace NBT.Domain.Enums;

/// <summary>
/// Represents the types of NBT tests available.
/// </summary>
public enum TestType
{
    /// <summary>
    /// Academic and Quantitative Literacy (AQL) test.
    /// Assesses academic literacy skills.
    /// </summary>
    AcademicLiteracy = 0,

    /// <summary>
    /// Quantitative Literacy (QL) test.
    /// Assesses quantitative reasoning skills.
    /// </summary>
    QuantitativeLiteracy = 1,

    /// <summary>
    /// Mathematics (MAT) test.
    /// Assesses mathematical problem-solving skills.
    /// </summary>
    Mathematics = 2
}
