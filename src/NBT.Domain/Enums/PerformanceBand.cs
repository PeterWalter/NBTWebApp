namespace NBT.Domain.Enums;

/// <summary>
/// Represents the performance band achieved in an NBT test.
/// Performance bands indicate the level of proficiency demonstrated.
/// </summary>
public enum PerformanceBand
{
    /// <summary>
    /// Elementary proficiency level.
    /// Indicates basic understanding with significant room for improvement.
    /// </summary>
    Elementary = 0,

    /// <summary>
    /// Basic proficiency level.
    /// Indicates foundational understanding of concepts.
    /// </summary>
    Basic = 1,

    /// <summary>
    /// Intermediate proficiency level.
    /// Indicates good understanding and application of concepts.
    /// </summary>
    Intermediate = 2,

    /// <summary>
    /// Proficient level.
    /// Indicates strong understanding and advanced application of concepts.
    /// </summary>
    Proficient = 3
}
